# 材確 (Zaikaku) 取扱説明書

1. はじめに
「材確（ザイカク）」は、製造現場における材料取り間違い防止アプリです。
製品（指示書）と材料（現品）のバーコードを照合し、正誤を判定します。

2. 基本的な使い方

2.1 トップ画面
- スキャン開始: 照合画面へ移動します。
- 製品マスタ: 製品・材料の登録を行います。
- 履歴閲覧: 過去の結果を確認できます。

2.2 準備（製品登録）
1. 「製品マスタ」>「＋」をタップ。
2. 製品名・型番を入力。
3. 材料バーコードを登録（手入力または画像スキャン）。
4. 保存。

3. 照合フロー
1. 「スキャン開始」をタップ。
2. 画面上部で「製品」を選択。
3. バーコードをカメラでスキャン。
4. OK/NG結果を確認し、次へ。

4. 履歴と出力
履歴画面から、期間を指定してCSVデーターをエクスポート（共有）できます。

5. よくある質問
Q. スキャンできない
A. カメラの汚れ、明るさを確認してください。

Q. 「未登録」と出る
A. 選択中の製品に登録されていないバーコードです。
